class Solution {
public: //T.C: O(n) and S.C: O(1)
    int maxProfit(vector<int>& prices) {
        int total_profit = 0;

        for(int i=0;i<prices.size()-1;i++){
            if(prices[i+1] > prices[i]){
                total_profit += prices[i+1] - prices[i];
            }
        }
        return total_profit;
    }
};